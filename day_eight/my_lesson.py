# Подробное объяснение
# Работа с файлами в Python состоит из нескольких шагов. Вот как это делается:

# 1. Открытие файла:
# file = open("имя_файла", "режим")
# "имя_файла" - это имя файла, с которым вы хотите работать.
# "режим" - это то, что вы хотите сделать с файлом: читать, писать, добавлять и т.д.
# "r" - чтение (read).
# "w" - запись (write).
# "a" - добавление (append).
# "b" - бинарный режим (для работы с файлами, содержащими не текстовую информацию).

# 2. Чтение из файла:
# content = file.read()
# print(content)

# read() считывает весь контент файла и возвращает его как строку.

# 3. Запись в файл:
# file = open("имя_файла", "w")
# file.write("Ваш текст здесь")
# file.close()

# write() записывает текст в файл. Если файла нет, 
# он будет создан. Если файл уже есть, его содержимое будет перезаписано.

# 4. Добавление в файл:
# file = open("имя_файла", "a")
# file.write("Новый текст здесь")
# file.close()
# a - append() добавляет текст в конец файла.

# 5. Закрытие файла:
# file.close()
# close() закрывает файл, чтобы сохранить изменения и освободить ресурсы.

# Объединённый и понятный вариант

